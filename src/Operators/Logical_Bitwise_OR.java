// Logical - checks the 2nd condition only if 1st condition is false
// bitwise - checks both the condition whether the 1st condition is true/false

package Operators;

public class Logical_Bitwise_OR {

	public static void main(String[] args) {
	    int j = 34;
	    int k = 43;
	    int h = 23;
	    System.out.println(k>j || j>h);
	    System.out.println(j>k | j>h);
	    

	}

}
