package Operators;

public class ArithmeticOperator {

	public static void main(String[] args) {
		int a = 4;
		int g = 2;
		System.out.println(a*g); 
		System.out.println(a/g);  
		System.out.println(a+g);  
		System.out.println(a-g);
		System.out.println(a%g);
		
		//Experssion
		System.out.println("Expression");
		System.out.println(a+g*g-a/a%g);

	}

}



